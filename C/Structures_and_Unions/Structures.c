struct course {
	int id;
	char title[40];
	float hours;
};

/*
 * A structure is a data type created by the user.
 * A structure declaration has, the keyword "struct", the "structure tag", the curly braces and a list of variables called "members".
 * Don't forget the ";" at the end of the structure.
 * The data types can be anything, ints, floats, chars, arrays, pointers, strings, other structures etc.
*/