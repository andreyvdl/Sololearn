/*
 * The preprocessor use "# directive" before compiling the program.
 * They can be:
 * "#include" include header files.
 * "#define, #undef" define or undefine a macro.
 * "#ifdef, #ifndef, #if, #else, #elif, #endif" condicional compilation.
 * "#pragma" implementation and compiler specific.
 * "#error, #warning" output an error or warning.
*/