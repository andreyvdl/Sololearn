/*
Arithmetic Operators: 
"+" addition 5 + 5 = 10;
"-" subtraction 5 - 5 = 0;
"*" multiplication 5 * 5 = 25
"/" division 5 / 5 = 1;
"%" modulus 5 % 5 = 0;
"++" increment a = 5; a++; a -> 6 | before the variable is pre increment, after is a post increment;
"--" decrement a = 5; a--; a -> 4 | before the variable is pre decrement, after is a post decrement;
*/
var x = 10 + 5;
console.log(x);

var y = x + 5 + 22 + 45 + 6548;
console.log(y);

/*
You can also use "eval()" to get a result from a string expression;
*/
console.log(eval("10 * 20 + 8"));

console.log(38 % 5);